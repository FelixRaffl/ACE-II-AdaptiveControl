%% ---------------------------------------------------------- block scripts
function code = rlsScript(p)
    code = [ ...
        "function [a1_est,a0_est,b1_est,b0_est,traceP] = rls_estimator(y,y1,y2,u1,u2,alpha)" newline ...
        "%#codegen" newline ...
        "% RLS for y(k) = [-y(k-1), -y(k-2), u(k-1), u(k-2)] * theta." newline ...
        "% Use alpha = 1 here; alpha < 1 without persistent excitation causes covariance windup and bursting." newline ...
        "% The excitation gate skips idle updates, and the trace limit is a final guard." newline ...
        "persistent P theta" newline ...
        "if isempty(P)" newline ...
        "    P = " num(p.P0) " * eye(4);" newline ...
        "    theta = [-1.0; 0.2; 0.05; 0.02];" newline ...
        "end" newline ...
        "phi = [-y1; -y2; u1; u2];" newline ...
        "if (phi' * phi) > 1e-8" newline ...
        "    den = phi' * P * phi + alpha;" newline ...
        "    L = (P * phi) / den;" newline ...
        "    thetaNew = theta + L * (y - phi' * theta);" newline ...
        "    PNew = (eye(4) - L * phi') * P / alpha;" newline ...
        "    PNew = 0.5 * (PNew + PNew');" newline ...
        "    trNew = trace(PNew);" newline ...
        "    if isfinite(trNew) && all(isfinite(thetaNew))" newline ...
        "        theta = thetaNew;" newline ...
        "        P = PNew;" newline ...
        "    end" newline ...
        "end" newline ...
        "tr = trace(P);" newline ...
        "if tr > " num(p.tracePLimit) newline ...
        "    P = P * (" num(p.tracePLimit) " / tr);" newline ...
        "    tr = trace(P);" newline ...
        "end" newline ...
        "a1_est = theta(1);" newline ...
        "a0_est = theta(2);" newline ...
        "b1_est = theta(3);" newline ...
        "b0_est = theta(4);" newline ...
        "traceP = tr;" newline ...
        "end"];
end

function code = polePlacementScript()
    code = [ ...
        "function [c0,d2,d1,d0] = pole_placement(a1,a0,b1,b0,q0,q1,q2,q3,kFreeze)" newline ...
        "%#codegen" newline ...
        "% From (z^2+a1*z+a0)(z-1)(z+c0) +" newline ...
        "% (b1*z+b0)(d2*z^2+d1*z+d0) = z^4+q3*z^3+q2*z^2+q1*z+q0." newline ...
        "% Matching z^3..z^0 coefficients gives M*[c0; d2; d1; d0] = rhs." newline ...
        "persistent c0Hold d2Hold d1Hold d0Hold k" newline ...
        "if isempty(k)" newline ...
        "    c0Hold = 0.0;" newline ...
        "    d2Hold = 0.2;" newline ...
        "    d1Hold = 0.0;" newline ...
        "    d0Hold = 0.0;" newline ...
        "    k = 0.0;" newline ...
        "end" newline ...
        "if k >= kFreeze" newline ...
        "    M = [1.0,     b1, 0.0, 0.0;" newline ...
        "         a1-1.0,  b0, b1,  0.0;" newline ...
        "         a0-a1,  0.0, b0,  b1;" newline ...
        "        -a0,     0.0, 0.0, b0];" newline ...
        "    rhs = [q3 - a1 + 1.0; q2 - a0 + a1; q1 + a0; q0];" newline ...
        "    if rcond(M) >= 1e-8" newline ...
        "        sol = M \ rhs;" newline ...
        "        if all(isfinite(sol))" newline ...
        "            c0Hold = sol(1);" newline ...
        "            d2Hold = sol(2);" newline ...
        "            d1Hold = sol(3);" newline ...
        "            d0Hold = sol(4);" newline ...
        "        end" newline ...
        "    end" newline ...
        "end" newline ...
        "k = k + 1.0;" newline ...
        "c0 = c0Hold;" newline ...
        "d2 = d2Hold;" newline ...
        "d1 = d1Hold;" newline ...
        "d0 = d0Hold;" newline ...
        "end"];
end

%% ---------------------------------------------------------- local helpers
function setFunctionScript(blockPath, code)
    rt = sfroot;
    chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', blockPath);
    if isempty(chart), error('MATLAB Function chart not found: %s', blockPath); end
    chart.Script = char(code);
end

function logSignal(blockPath, portNumber, signalName)
    ports = get_param(blockPath, 'PortHandles');
    set_param(ports.Outport(portNumber), 'DataLogging', 'on', ...
        'DataLoggingNameMode', 'Custom', 'DataLoggingName', signalName);
end

function configureScope(scopePath, nAxes, showLegend)
    set_param(scopePath, 'NumInputPorts', num2str(nAxes));
    sc = get_param(scopePath, 'ScopeConfiguration');
    sc.LayoutDimensions = [nAxes 1];
    sc.OpenAtSimulationStart = false;
    sc.ShowLegend = logical(showLegend);
end

function addNote(mdl, text, pos)
    h = Simulink.Annotation(mdl, text);
    h.position = pos;
end
