%% Build the second-order adaptive DC-motor simulation model.
%
% Builds one self-contained Simulink model into ACE_OUT_DIR (default: pwd):
%   adaptive_dcmotor_2nd_sim.slx
%
% The model uses an exact ZOH-discretized second-order motor plant, a
% two-phase speed reference for excitation and tracking validation,
% 4-parameter RLS estimation and an RST controller with integral action
% from the fourth-order Diophantine pole-placement equation.
%
% Usage:
%   matlab.exe -wait -nosplash -sd <stage> -batch "build_second_order_model"

outDir = getenv('ACE_OUT_DIR');
if isempty(outDir), outDir = pwd; end

p = secondOrderParams();
buildSecondOrderSimModel(p, outDir);
disp('BUILD_OK');

%% ---------------------------------------------------------------- params
function p = secondOrderParams()
    p.Km = 0.05;
    p.J  = 0.001;
    p.b  = 0.0005;
    p.La = 0.002;
    p.Ra = 2.0;

    p.T       = 0.002;  % 2 ms - resolves the electrical pole
    p.alpha   = 1.0;    % no forgetting; alpha < 1 without PE causes P windup/bursting
    p.P0      = 100;    % P = P0 * eye(4)
    p.uMax    = 12;     % armature voltage limit [V]
    p.kFreeze = 50;     % apply pole placement only after 50 samples
    p.tEnd    = 8;      % simulation horizon [s]
    p.tPhase1 = 4;      % PRBS excitation horizon [s]

    p.refLow     = 60;
    p.refHigh    = 120;
    p.prbsHold   = 5;   % bit duration in samples, 5*T = 10 ms
    p.tracePLimit = 9000;

    p.pFast = 0.1357;   % electrical plant pole at T = 2 ms
    r = exp(-p.T / 0.05);
    % Design rule: modes much faster than the desired bandwidth are not
    % moved; otherwise the controller must invert the fast dynamics and can
    % become unstable, which necessarily diverges behind saturation.
    q = poly([0 p.pFast r r]);
    p.q3 = q(2);
    p.q2 = q(3);
    p.q1 = q(4);
    p.q0 = q(5);

    Gd = c2d(tf(p.Km, [p.La*p.J, p.La*p.b + p.Ra*p.J, p.Ra*p.b + p.Km^2]), ...
        p.T, 'zoh');
    [numd, dend] = tfdata(Gd, 'v');
    numd = numd / dend(1);
    dend = dend / dend(1);
    p.a1 = dend(end-1);
    p.a0 = dend(end);
    p.b1 = numd(end-1);
    p.b0 = numd(end);
end

function s = num(v)
    s = sprintf('%.10g', v);
end

function s = vec(v)
    s = ['[' strjoin(arrayfun(@num, v(:).', 'UniformOutput', false), ' ') ']'];
end

%% ------------------------------------------------ second-order simulation
function buildSecondOrderSimModel(p, outDir)
    mdl = 'adaptive_dcmotor_2nd_sim';
    if bdIsLoaded(mdl), close_system(mdl, 0); end
    new_system(mdl);
    set_param(mdl, ...
        'SolverType', 'Fixed-step', ...
        'Solver', 'FixedStepDiscrete', ...
        'FixedStep', num(p.T), ...
        'StopTime', num(p.tEnd), ...
        'SignalLogging', 'on', ...
        'SignalLoggingName', 'logsout', ...
        'Location', [100 100 1450 720]);

    refProfile = buildReferenceProfile(p);
    mdlWks = get_param(mdl, 'ModelWorkspace');
    assignin(mdlWks, 'refProfile', refProfile);

    add_block('simulink/Sources/From Workspace', [mdl '/Reference profile'], ...
        'VariableName', 'refProfile', ...
        'SampleTime', num(p.T), ...
        'Interpolate', 'off', ...
        'OutputAfterFinalValue', 'Holding final value', ...
        'Position', [45 105 155 145]);

    ctrl = [mdl '/Adaptive RST controller'];
    add_block('simulink/Ports & Subsystems/Subsystem', ctrl, ...
        'Position', [315 60 555 245]);
    Simulink.SubSystem.deleteContents(ctrl);
    buildControllerSubsystem(ctrl, p);

    add_block('simulink/Discrete/Discrete Transfer Fcn', [mdl '/ZOH motor plant'], ...
        'Numerator', vec([p.b1 p.b0]), ...
        'Denominator', vec([1 p.a1 p.a0]), ...
        'SampleTime', num(p.T), ...
        'Position', [640 105 760 165]);

    add_block('simulink/Signal Routing/Mux', [mdl '/Tracking mux'], ...
        'Inputs', '3', 'Position', [875 75 880 195]);
    add_block('simulink/Sinks/Scope', [mdl '/Tracking scope'], ...
        'Position', [940 105 990 155]);
    configureScope([mdl '/Tracking scope'], 1, true);

    add_block('simulink/Signal Routing/Mux', [mdl '/Parameter mux'], ...
        'Inputs', '4', 'Position', [875 300 880 430]);
    add_block('simulink/Sinks/Scope', [mdl '/Parameter scope'], ...
        'Position', [940 330 990 380]);
    configureScope([mdl '/Parameter scope'], 1, true);

    add_block('simulink/Sinks/Scope', [mdl '/Covariance scope'], ...
        'Position', [940 495 990 545]);
    configureScope([mdl '/Covariance scope'], 1, false);

    add_line(mdl, 'Reference profile/1', 'Adaptive RST controller/1', 'autorouting', 'on');
    add_line(mdl, 'Adaptive RST controller/1', 'ZOH motor plant/1', 'autorouting', 'on');
    add_line(mdl, 'ZOH motor plant/1', 'Adaptive RST controller/2', 'autorouting', 'on');

    add_line(mdl, 'Reference profile/1', 'Tracking mux/1', 'autorouting', 'on');
    add_line(mdl, 'ZOH motor plant/1', 'Tracking mux/2', 'autorouting', 'on');
    add_line(mdl, 'Adaptive RST controller/1', 'Tracking mux/3', 'autorouting', 'on');
    add_line(mdl, 'Tracking mux/1', 'Tracking scope/1', 'autorouting', 'on');

    add_line(mdl, 'Adaptive RST controller/2', 'Parameter mux/1', 'autorouting', 'on');
    add_line(mdl, 'Adaptive RST controller/3', 'Parameter mux/2', 'autorouting', 'on');
    add_line(mdl, 'Adaptive RST controller/4', 'Parameter mux/3', 'autorouting', 'on');
    add_line(mdl, 'Adaptive RST controller/5', 'Parameter mux/4', 'autorouting', 'on');
    add_line(mdl, 'Parameter mux/1', 'Parameter scope/1', 'autorouting', 'on');
    add_line(mdl, 'Adaptive RST controller/6', 'Covariance scope/1', 'autorouting', 'on');

    logSignal([mdl '/Reference profile'], 1, 'ref');
    logSignal([mdl '/ZOH motor plant'], 1, 'y');
    logSignal([mdl '/Adaptive RST controller'], 1, 'u');
    logSignal([mdl '/Adaptive RST controller'], 2, 'a1_est');
    logSignal([mdl '/Adaptive RST controller'], 3, 'a0_est');
    logSignal([mdl '/Adaptive RST controller'], 4, 'b1_est');
    logSignal([mdl '/Adaptive RST controller'], 5, 'b0_est');
    logSignal([mdl '/Adaptive RST controller'], 6, 'traceP');

    addNote(mdl, ['Second-order adaptive DC-motor simulation.' newline ...
        'Exact ZOH plant, T = ' num(p.T) ' s, PRBS reference 60/120 rad/s until ' ...
        num(p.tPhase1) ' s, then steps 90/130/90 rad/s.' newline ...
        'RLS theta = [a1; a0; b1; b0], P0 = ' num(p.P0) ...
        ', alpha = ' num(p.alpha) ', u in [' num(-p.uMax) ', ' num(p.uMax) '] V.'], [40 20]);
    addNote(mdl, ['True ZOH coefficients for y(k) + a1*y(k-1) + a0*y(k-2) = ' ...
        'b1*u(k-1) + b0*u(k-2):' newline ...
        'a1 = ' num(p.a1) ', a0 = ' num(p.a0) ...
        ', b1 = ' num(p.b1) ', b0 = ' num(p.b0) newline ...
        'Desired polynomial q(z) = z*(z - pFast)*(z - exp(-T/0.05))^2 = ' ...
        'z^4 + q3*z^3 + q2*z^2 + q1*z + q0.'], [40 580]);

    save_system(mdl, fullfile(outDir, [mdl '.slx']));
    close_system(mdl, 0);
    fprintf('Built %s\n', fullfile(outDir, [mdl '.slx']));
end

function refProfile = buildReferenceProfile(p)
    t = (0:p.T:p.tEnd).';
    ref = zeros(size(t));

    reg = ones(1, 7);
    holdCount = 0;
    level = p.refLow;
    for k = 1:numel(t)
        if t(k) < p.tPhase1
            if holdCount <= 0
                outBit = reg(7);
                newBit = double(xor(reg(7) > 0.5, reg(6) > 0.5));
                reg(7) = reg(6);
                reg(6) = reg(5);
                reg(5) = reg(4);
                reg(4) = reg(3);
                reg(3) = reg(2);
                reg(2) = reg(1);
                reg(1) = newBit;
                level = p.refLow + (p.refHigh - p.refLow) * outBit;
                holdCount = p.prbsHold;
            end
            ref(k) = level;
            holdCount = holdCount - 1;
        elseif t(k) < 5.5
            ref(k) = 90;
        elseif t(k) < 7.0
            ref(k) = 130;
        else
            ref(k) = 90;
        end
    end

    refProfile = [t, ref];
end

